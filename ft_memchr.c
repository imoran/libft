#include "libft.h"

void	*ft_memchr(const void *s, int c, size_t n)
{
	const char	*s1;

	s1 = (const char *)s;
	while (n--)
	{
		if (*s1 == c)
			return ((void *)s1);
		s1++;
	}
	return (0);

}
