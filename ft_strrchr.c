#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	char 	*s2;
	int 	len;

	s2 = (char *)s;
	len = 0;
	while (*s2)
	{
		s2++;
		len++;
	}
	while (len--)
	{
		if (*s2 == c)
			return (s2);
		s2--;
	}
	return (0);
}
